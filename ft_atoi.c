#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int sign;

	n = 0;
	i = 0;
	sign = 1;
	if (ft_strcmp(str, "-2147483648") == 0)
		return (-2147483648);
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = 10 * n + (str[i] - '0');
		i++;
	}
	return (n * sign);
}
