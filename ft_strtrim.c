#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	w1;
	size_t	w2;

	if (!s)
		return (0);
	w1 = 0;
	while ((s[w1] == ' ' || s[w1] == '\n' || s[w1] == '\t'))
		w1++;
	if (s[w1] == '\0')
		return (ft_strdup(""));
	w2 = ft_strlen(s) - 1;
	while ((s[w2] == ' ' || s[w2] == '\n' || s[w2] == '\t') && w2 > 0)
		w2--;
	return (ft_strsub(s, w1, w2 - w1 + 1));
}
